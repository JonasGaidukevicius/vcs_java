package lt.gaidukevicius;

//Cia yra savotiska receptura. Kintamieji laikys reiksmes, kiek reikia kokiu ingridientu, kad pagaminti kazkokia pica
public class Pica {
	private int padas;
	private int suris;
	private int grybai;
	private int kumpis;
	private int desra;
	private int padazas;
	
	//seteriai ir geteriai
	public int getPadas() {
		return padas;
	}
	public void setPadas(int padas) {
		this.padas = padas;
	}
	public int getSuris() {
		return suris;
	}
	public void setSuris(int suris) {
		this.suris = suris;
	}
	public int getGrybai() {
		return grybai;
	}
	public void setGrybai(int grybai) {
		this.grybai = grybai;
	}
	public int getKumpis() {
		return kumpis;
	}
	public void setKumpis(int kumpis) {
		this.kumpis = kumpis;
	}
	public int getDesra() {
		return desra;
	}
	public void setDesra(int desra) {
		this.desra = desra;
	}
	public int getPadazas() {
		return padazas;
	}
	public void setPadazas(int padazas) {
		this.padazas = padazas;
	}
	
	 
	
}
